private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
    String search = SearchField.getText();
     String parameter = (String) SearchParameters.getSelectedItem();
    try{
        Class.forName("com.mysql.jdbc.Driver");
        Connection conect = DriverManager.getConnection("jdbc:mysql://localhost:3306/integratedfarmsystemnnew", "root", "");
        Statement stmt = conect.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,
                ResultSet.CONCUR_READ_ONLY);
        ResultSet srs = stmt.executeQuery(
                "SELECT * FROM cattlefeeding where "+parameter+" =\"" +search+"\"");
        while (srs.next()) {

            String number = srs.getString("barnID");
            String date = srs.getString("date");
            String quantity = srs.getString("quantity");
            String particulars = srs.getString("particulars");
            String storeID = srs.getString("storeID");

          tableData.add(toVector(number,date,quantity,particulars,storeID));

            }
            Vector columns = new Vector();
            columns.add("BarnID");
            columns.add("Date");
            columns.add("Quantity");
            columns.add("Particulars");
            columns.add("StoreID");
            SearchTable1.setModel(new DefaultTableModel(tableData, columns));
        }

    catch(SQLException e) {
            System.out.println("2 Error : "+e);
        } catch(Exception ex) {
            System.out.println("Error 1:"+ex);
        }
}//GEN-LAST:event_SearchButtonActionPerformed

 private Vector toVector(String number, String date,String quantity,  String particulars, String storeID)
     {
        Vector vector = new Vector();
        vector.add(number);
        vector.add(date);
        vector.add(quantity);
        vector.add(particulars);
        vector.add(storeID);
        return vector;
      }

