        int option = JOptionPane.showConfirmDialog(null, "Are you sure yo want to delete the useraccount "+ UsernameToDelete+" ?");
        if (option == JOptionPane.YES_OPTION) {
            try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection conect = DriverManager.getConnection("jdbc:mysql://localhost:3306/integratedfarmsystemnnew", "root", "");
                PreparedStatement pstmt = conect.prepareStatement("DELETE FROM loginuser WHERE username = ?");
                pstmt.setString(1, UsernameToDelete);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "The user account has been deleted");
            } catch(SQLException e) {
                System.out.println("2 Error : "+e);
            } catch(Exception ex) {
                System.out.println("Error 1:"+ex);
            }
        }
